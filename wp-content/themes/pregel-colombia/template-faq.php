		<h3>¿Qué es una Base?</h3>
		Las Bases de PreGel son ingredientes esenciales bien equilibrados, en forma de polvo, que promueven el sabor y textura de más alta calidad así como una fácil producción del Gelato. La Base es fundamental para crear una estructura y textura aterciopeladas que permitan la cremosidad y suavidad deseadas de las que se enorgullece PreGel.
		<h3>¿Qué es un estabilizante?</h3>
		Existen dos tipos de estabilizantes utilizados en el Gelato. Uno proviene de fuentes animales y el más comúnmente utilizado es la gelatina. El otro estabilizante es una fuente vegetal como Carragenano, Goma de Guar y Goma de Garrofín. Los estabilizantes se utilizan en el Gelato por una variedad de razones, pues incrementan la viscosidad, mejoran la suavidad del cuerpo del producto e impiden que se formen cristales de hielo y que se derrita.
		<h3>¿Qué es el Arabeschi<sup>&reg;</sup>?</h3>
		El Arabeschi<sup>&reg;</sup> es un topping y relleno para el Gelato. Como topping se utiliza para mejorar la decoración del Gelato en tarrinas y bandejas, así como los Semifreddos. Y como relleno ayuda a darle textura y sabor adicional al Gelato. Entre algunos de nuestros mejores Arabeschi<sup>&reg;</sup> se encuentran el Donatella Rock, el Crocante de Café y diferentes tipos de Frutas Confitadas.
		<h3>¿Cuáles son los ingredientes básicos para fabricar Gelato?</h3>
		En la explicación más básica, el Gelato se hace mezclando leche o agua, azúcar y otros ingredientes naturales (como emulsionantes y estabilizantes) que integran la Base, luego se agregan nuestros distintivos sabores de Crema o Fruta.
		<h3>¿Cuál es la diferencia entre el proceso frío y el caliente?</h3>
		El proceso caliente requiere de pasteurización, mientras que el proceso frío no. Para saber más sobre estos diferentes procesos, asista a una de nuestras clases de capacitación.
		<h3>¿Cuánta pasta de sabor necesito utilizar?</h3>
		La cantidad de pasta de sabor para un tamaño estándar de lote de 5 litros se puede encontrar en nuestro Libro de Recetas. El Libro de Recetas se puede encontrar aquí, en el sitio en Internet, o se le puede enviar como PDF por correo electrónico ya sea por medio de su gerente de ventas o por un miembro del Equipo de Servicios Especiales. Para los lotes especiales, por favor póngase en contacto con nuestro Equipo de Servicios Especiales, que puede prestarle una mayor ayuda.
		<h3>¿Durante cuánto tiempo puedo guardar mi Gelato en la vitrina?</h3>
		Esta respuesta variará ligeramente dependiendo de diferentes factores durante la preparación, pero por lo general entre 3 y 5 días. Esto será verdad si guarda su Gelato en el congelador por la noche. Quizá consiga también una mayor vida útil si prepara su Gelato con el proceso caliente. La forma preferida de servir el Gelato es cuando se encuentra en su estado más fresco, así que siempre alentamos a nuestros clientes a que sigan ese consejo lo más al pie de la letra que puedan.
		<h3>¿Cuál es la temperatura para el almacenaje?</h3>
		La temperatura para almacenar en el congelador debe de ser de aproximadamente -17.7 grados centígrados.
		<h3>¿A qué temperatura tiene que estar el Gelato en cuanto sale del congelador discontinuo?</h3>
		A entre -7.7 y -5 grados centígrados. En este punto, sólo entre el 50 y el 60 por ciento del agua está congelado.
		<h3>¿Cómo presenta el Gelato?</h3>
		Por lo general en contenedores de 5 litros. Sale del congelador discontinuo de manera muy natural, sin presionar o empujarlo demasiado. El resultado debe ser una forma muy armoniosa con muchas ondas y rizos.
		<h3>¿Cómo sirve el Gelato?</h3>
		Aunque con el helado se suelen utilizar palas, el Gelato se sirve con espátulas, que le permiten adoptar una cualidad más artesana.
		<h3>¿Decora el Gelato?</h3>
		Sin duda. El Gelato está considerado un postre artesanal, lo cual significa que la decoración forma parte importante de toda la atmósfera de la experiencia del Gelato. El Arabeschi<sup>&reg;</sup> y los Toppings de PreGel se usan ambos para decorar el producto acabado.
		<h3>¿Durante cuánto tiempo durarán mis productos PreGel?</h3>
		Los productos PreGel tienen una vida útil de tres años a partir del momento en que son producidos; el que se hayan abierto o no tiene ninguna incidencia en la vida útil. Sin embargo, recuerde guardar los productos bien sellados y no los almacene en temperaturas extremas. Pueden acortar su periodo de vida. La fecha de caducidad se indica en la presentación y tiene la forma europea, que es día/mes/año. Si tiene dudas con respecto a la vida del producto, póngase en contacto con nuestro Equipo de Servicios Especiales.
		<h3>¿Qué debo hacer si se empiezan a separar la Pasta, el Arabeschi<sup>&reg;</sup> o los Toppings (el aceite de la pasta)?</h3>
		Mézclelos bien y de vez en cuando para evitar la separación. Agite bien los Toppings antes de servir.
		<h3>¿Cuál es el contenido nutricional de mi producto?</h3>
		PreGel sólo puede proporcionar información nutricional sobre los productos no acabados que produce. Si quiere evaluar los contenidos nutricionales del producto acabado que usted proporciona, le recomendamos que se asocie con un laboratorio local de pruebas que pueda proporcionarle con exactitud la información nutricional sobre su producto.


		<h3>What are the basic ingredients in gelato?</h3>
		<p>Gelato is made up of a liquid and solid raw ingredient, stabilisers, emulsifiers, flavour ingredients, air and other ingredients.  For example:</p>
		<ul>
			<li>Liquid Raw Ingredients: Water, milk, etc.</li>
			<li>Solid Raw Ingredients: Sugars, fats, Milk Solids Non-Fat (MSNFs)</li>
			<li>Stabilisers: Guar Gum, locust bean gum, etc.</li>
			<li>Emulsifiers: Mono- and di-glycerides</li>
			<li>Flavour Ingredients: Pastes, powders</li>
			<li>Air</li>
			<li>Other Ingredients: Mineral salts, cocoa products, dry fruits, toppings and fillers</li>
		</ul>
		<br>
		<h3>What is a Base?</h3>
		<p>PreGel Bases are well-balanced essential ingredients in powdered form that promote the highest quality flavour, texture and ease-of-use for gelato and ice cream production. The base is pivotal for creating a velvety structure and texture that allows for the desirable creaminess and softness that PreGel prides itself on.</p>
		<br>
		<h3>What is a Stabiliser?</h3>
		<p>There are two types of stabilisers used in gelato and ice cream. One comes from animal sources and the most commonly used is gelatin or eggs. The other stabiliser is a vegetable source such as carrageenan, guar gum and locust bean gum. Stabilisers are used in gelato and ice cream for a variety of reasons, as they increase viscosity, improve the smoothness of the product body, and prevent ice crystal formation and melting.</p>
		<br>
		<h3>What is Arabeschi®?</h3>
		<p>Arabeschi® is a topping and filling for gelato, sorbetto, ice cream, frozen yogurt, soft serve, pastry and frozen desserts, and beverages. As a topping, it is used to enhance the decoration of desserts. As a filling, it adds extra flavour and textural components to desserts or savory items. Our Arabeschi® line encompasses a variety of flavours including rich milk and dark chocolate as well as selectively sourced nut and fruit varieties.</p>
		<br>
		<h3>What are the basic ingredients to make gelato?</h3>
		<p>In the most basic explanation, gelato is made by mixing milk or water, sugar and other natural ingredients (such as emulsifiers and stabilisers) which make up the base. Our distinct cream or fruit flavours are then added.</p>
		<br>
		<h3>What is the difference between Hot and Cold Process?</h3>
		<p>The easiest answer to this question is that Hot Process requires the heating of liquids, whereas Cold Process does not. To find out more about these different processes, attend one of our training classes.</p>
		<br>
		<h3>How much flavour paste do I need to use?</h3>
		<p>The amount of flavour paste for a standard batch size of 5 liters varies by recipe.  A variety of recipe books can be downloaded from this site, or emailed to you by your sales or special services representative. For special batches, please contact our International Training Center.</p>
		<br>
		<h3>How long can I keep my gelato in the display case for?</h3>
		<p>This answer will vary slightly depending on various factors during preparation, but generally 3-5 days. This holds true if you are storing your gelato in a freezer overnight. You may also achieve a longer shelf-life by preparing your gelato via Hot Process. The preferred way to serve gelato is when it is at its freshest.</p>
		<p>What is the stocking temperature?  The stocking temperature in a freezer should be around -17.8˚.</p>
		<p>What temperature does gelato have to be once it comes out of the batch freezer?  Between -7,8˚ to -8˚C. At this point just 50-60 percent of water is frozen.</p>
		<br>
		<h3>How do you present gelato?</h3>
		<p> There are three ways to present gelato. We suggest sculpted, flat or mounds showcased in a 5 liter pan. Gelato generally comes out of the batch freezer in a very natural way, and does not require much pressing or pushing. The result should be a very harmonious form.</p>
		<br>
		<h3>How do you serve gelato?</h3>
		<p>While ice cream generally uses scoopers, gelato is served with spades or spatulas, which allow for it to take on more of an artisan appearance.</p>
		<br>
		<h3>Do you decorate gelato?</h3>
		<p>Absolutely. Gelato is considered an artisan dessert which means the decoration is a large part of the whole ambiance of the gelato experience. PreGel’s Arabeschi® and Toppings are both used to decorate the finished product. Additionally, PreGel’s molds can be used to create gelato flowers, gelato turtles and much more, taking the gelato pan from visually appealing to stunning.</p>
		<br>
		<h3>How long will my PreGel products last?</h3>
		<p>PreGel products have a shelf-life of 2-3 years once produced – whether it’s been opened or not has no bearing on the shelf-life. Although remember to keep products well sealed and do not keep in extreme temperatures, as both can shorten the life span and reduce the quality of the product. The expiration date is indicated on your packaging and is in European form, which is day/month/year. If you have questions on the product life, contact our Special Service Team.</p>
		<br>
		<h3>What should I do if a Paste, Arabeschi® or Topping begins to separate (oil from paste)?</h3>
		<p>This is generally true for most nut and chocolate products, as they carry natural oils within. To 	prevent separation, occasionally mix it like a soup. Shake toppings well.</p>
		<br>
		<h3>What is the nutritional content for my product?</h3>
		<p>PreGel can only provide nutritional information for our ingredient solutions. If you want to assess the nutritional content for the finished product you provide, we recommend that you partner with a local testing lab that can accurately provide nutritional information on your product.</p>
		<h3>Why is my Gelato Hard?</h3>
		<p>Check Display Cabinet and ensure that it is running at -14C to -16C,
		Otherwise check recipe and INCREASE sugar level</p>
		<h3>Why is my Gelato soft?</h3>
		<p>Check Display Cabinet and ensure that it is running at -14C to -16C,
		Otherwise check recipe and DECREASE sugar level</p>
		<h3>Why is my Gelato Grainy/Crumbling?</h3>
		<p>The problem may be related to the sugar content. Sugar level used may need to be INCREASED.High acidity level in the fruit. Not enough fibre in the Gelato. Base may not have rested or matured for long enough prior to churning.</p>
		<h3>How long should I let my Gelato Mix sit before churning?</h3>
		<p>Hot Process – Ideally overnight in the pasteurizer. Cold Process – Ideally 1 to 6 hours in the refrigerator.</p>
		<h3>What is Cold Process?</h3>
		<p>A Base that can be prepared simply by adding cold milk to it.</p>
		<h3>What is Hot Process?</h3>
		<p>A Base that can is prepared in a pasteurizer.</p>
		<h3>When do I use Dextrose?</h3>
		<p>When you need to increase sugar level without increasing the actual sweetness of the product.  Dextrose is used together with sugar normally. When using dextrose, the  Normal sugar being used should be adjusted down to accommodate Dextrose.</p>
		<h3>Should I use normal sugar or can I use Castor sugar?</h3>
		<p>Normal sugar use is okay as is Castor sugar? Castor sugar can be more expensive.</p>
		<h3>What is the purpose of Velutina?</h3>
		<p>Velutina is an emulsifier and helps to bring all ingredients together so as the end result is consistent, creamy and pliable (easy to serve). Velutina also creates air, therefore increases volume.</p>
		<h3>What is the purpose of Proteingel?</h3>
		<p>Is added to gelato base so as to add strength and used in the place of fats ie - butter, eggs etc.</p>
		<h3>What is Piu Cremosso?</h3>
		<p>This is a cream / fat supplement that assist in adding body to gelato.</p>
		<h3>What is Fibraplus</h3>
		<p>This is a fibre supplement that is used when fresh fruit  has been added to gelato . It will ensure firmness and body to the end product.</p>
		<h3>What Flavours/Pastes can I use with the Diabetic Base?</h3>

		<h3>What is a Base?</h3>
		PreGel Bases are well-balanced essential ingredients in powdered form that promote the highest quality flavor, texture and ease-of-use for gelato and ice cream production. The base is pivotal for creating a velvety structure and texture that allows for the desirable creaminess and softness that PreGel prides itself on.
		<h3>What is a Stabilizer?</h3>
		There are two types of stabilizers used in gelato and ice cream. One comes from animal sources and the most commonly used is gelatin or eggs. The other stabilizer is a vegetable source such as carrageenan, guar gum and locust bean gum. Stabilizers are used in gelato and ice cream for a variety of reasons, as they increase viscosity, improve the smoothness of the product body, and prevent ice crystal formation and melting.
		<h3>What is Arabeschi<sup>&reg;</sup>?</h3>
		Arabeschi<sup>&reg;</sup> is a topping and filling for gelato, sorbetto, ice cream, frozen yogurt, soft serve, pastry and frozen desserts, and beverages. As a topping, it is used to enhance the decoration of desserts. As a filling, it adds extra flavor and textural components to desserts or savory items. Our Arabeschi<sup>&reg;</sup> line encompasses a variety of flavors including rich milk and dark chocolate as well as selectively sourced nut and fruit varieties.
		<h3>What are the basic ingredients to make gelato?</h3>
		In the most basic explanation, gelato is made by mixing milk or water, sugar and other natural ingredients (such as emulsifiers and stabilizers) which make up the base. Our distinct cream or fruit flavors are then added.
		<h3>What is the difference between Hot and Cold Process?</h3>
		The easiest answer to this question is that Hot Process requires the heating of liquids, whereas Cold Process does not. To find out more about these different processes, attend one of our training classes.
		How much flavor paste do I need to use? The amount of flavor paste for a standard batch size of 5 liters varies by recipe, which are provided at www.pregelrecipes.com.  A variety of recipe books can be downloaded from this site, or emailed to you by your sales or special services representative. For special batches, please contact our International Training Center.
		<h3>How long can I keep my gelato in the display case for?</h3>
		This answer will vary slightly depending on various factors during preparation, but generally 3-5 days. This holds true if you are storing your gelato in a freezer overnight. You may also achieve a longer shelf-life by preparing your gelato via Hot Process. The preferred way to serve gelato is when it is at its freshest.
		What is the stocking temperature?  The stocking temperature in a freezer should be around -17.8˚/-0.04˚F.
		What temperature does gelato have to be once it comes out of the batch freezer?  Between -7.8˚ to -5˚C/18˚F to 23˚F. At this point just 50-60 percent of water is frozen.
		<h3>How do you present gelato?</h3>
		There are three ways to present gelato. We suggest sculpted, flat or mounds showcased in a 5 liter pan. Gelato generally comes out of the batch freezer in a very natural way, and does not require much pressing or pushing. The result should be a very harmonious form.
		How do you serve gelato?  While ice cream generally uses scoopers, gelato is served with spades or spatulas, which allow for it to take on more of an artisan appearance.
		<h3>Do you decorate gelato?</h3>
		Absolutely. Gelato is considered an artisan dessert which means the decoration is a large part of the whole ambiance of the gelato experience. PreGel’s Arabeschi<sup>&reg;</sup> and Toppings are both used to decorate the finished product. Additionally, PreGel's molds can be used to create gelato flowers, gelato turtles and much more, taking the gelato pan from visually appealing to stunning.
		<h3>How long will my PreGel products last?</h3>
		PreGel products have a shelf-life of 2-3 years once produced – whether it’s been opened or not has no bearing on the shelf-life. Although remember to keep products well sealed and do not keep in extreme temperatures, as both can shorten the life span and reduce the quality of the product. The expiration date is indicated on your packaging and is in European form, which is day/month/year. If you have questions on the product life, contact our Special Service Team.
		<h3>What should I do if a Paste, Arabeschi<sup>&reg;</sup> or Topping begins to separate (oil from paste)?</h3>
		This is generally true for most nut and chocolate products, as they carry natural oils within. To prevent separation, occasionally mix it like a soup. Shake toppings well.
		<h3>What is the nutritional content for my product?</h3>
		PreGel can only provide nutritional information for our ingredient solutions. If you want to assess the nutritional content for the finished product you provide, we recommend that you partner with a local testing lab that can accurately provide nutritional information on your product.


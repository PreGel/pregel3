		<h3>What are PreGel’s most popular flavors?</h3>
		PreGel judges it’s most popular flavors through sales, however, that does not mean that these are the most popular in your area. Fan favorites include: Salted Caramel Super Sprint, Prontociocc Traditional Paste (Chocolate), Happy Yo Super Sprint (Yogurt), Strawberry Fortefrutto<sup>&reg;</sup>, Cacaopat (Chocolate), Cheesecake Super Sprint, Hazelnut Piemonte Traditional Paste (Dark Roasted Hazelnut), Mango Fortefrutto<sup>&reg;</sup> and more.
		<h3>Can I use milk with the Super Sprints instead of water? How much?</h3>
		Yes, but it depends on the Super Sprint. Some require the addition of milk while others require the addition of water. Generally, 2500g/88.2oz of liquid are added for gelato and 3,000g/105.8oz of liquid are added for soft serve. Please consult <a href="whttp://pregelrecipes.com/?=canada&=canada&lang=en">PreGel Recipes</a> or the product bag for information on which liquid to add and how much.
		<h3>Can I mix Traditional Pastes and Fortefrutto<sup>&reg;</sup> with Super Sprints?</h3>
		No. Traditional Pastes and Fortefrutto<sup>&reg;</sup> are the flavorings used in Hot Process and Cold Process to add to a mix of base, stabilizers and emulsifiers. Super Sprints are an instant, complete product that already contain the flavor, base, stabilizers and emulsifiers.
		<h3>What is the difference between the pistachio pastes?</h3>
		All of our pistachio pastes are high-quality and authentic, coming from a variety of regions throughout Italy. Crema Pistachio Traditional Paste (Pistachio &amp; Almond) has a vibrant green color with a hint of hazelnuts and almonds, while Pistachio Primavera Traditional Paste boasts the same bright color, but is the most economical of the three. And Green Pure Pistachio Traditional Paste (Slightly Roasted Pure Pistachio) contains notes of nuttiness and saltiness. You really can't go wrong with any of our pistachio paste options.
		<h3>What is the difference between the hazelnut pastes?</h3>
		We offer two hazelnut options, Hazelnut Piemonte Traditional Paste (Dark Roasted Hazelnut) and Hazelnut Regina Traditional Paste (Premium Light Toasted Hazelnut). Hazelnut Piemonte is made with hazelnuts from the Piedmonte region of Italy and carries an authentic and roasted hazelnut taste. Hazelnut Regina is 100% pure hazelnut paste that boasts a fresh hazelnut flavor. Both are top quality pastes, it is your preference in terms of which you use.
		<h3>What is the difference between the chocolate hazelnut pastes?</h3>
		A popular Italian flavor, we carry three different chocolate hazelnut options. Chocolate-Hazelnut Traditional Paste combines milk chocolate with real hazelnut flavor, while Gianduia Traditional Paste (Dark Chocolate Hazelnut) combines dark chocolate with hazelnuts blended into the paste. Gianduiotto Rock Traditional Paste (Chocolate &amp; Hazelnut Pieces) is a thicker chocolate paste the contains actual pieces of roasted hazelnuts.

